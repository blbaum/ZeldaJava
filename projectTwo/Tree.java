public class Tree 
{
  private int x, y, width, height;

  public Tree(int x, int y)
  {
    this.x = x;
    this.y = y;
    this.width = 50;
    this.height = 50;
  }

  public int getX(){
    return this.x;
  }
  
  public int getY(){
    return this.y;
  }

  public int getWidth(){
    return this.width;
  }
  
  public int getHeight(){
    return this.height;
  }
}